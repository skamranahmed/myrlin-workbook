#!/usr/bin/env node
/**
 * Regenerate test/fixtures/codex-rollout-schema.json from the Codex CLI.
 *
 * Usage:
 *   node scripts/regen-codex-schema.js
 *
 * Behavior:
 *   - On a machine WITHOUT a Codex CLI, exits 1 with a clear error.
 *   - On a machine WITH Codex installed but lacking the
 *     `app-server generate-internal-json-schema` subcommand (older releases),
 *     exits 1 with a clear error pointing at the supported version.
 *   - On a supported install, calls the CLI to generate the schema, writes
 *     it to test/fixtures/codex-rollout-schema.json with stable formatting,
 *     and exits 0.
 *
 * Phase 17 Plan 17-01 (CDX-09). Phase 20 will wire a CI step that runs
 * this script and `git diff --exit-code test/fixtures/codex-rollout-schema.json`
 * to fail builds when an updated Codex CLI changes the canonical schema
 * without a corresponding parser update.
 *
 * NOTE: the literal Codex CLI binary name appears below. This file lives in
 * scripts/ which is OUTSIDE src/ so the grep gate (test/grep-gate.test.js)
 * does NOT walk it; no allowlist marker is required.
 *
 * SPDX-License-Identifier: AGPL-3.0-only
 */

'use strict';

const { execSync } = require('child_process');
const fs = require('fs');
const path = require('path');

const REPO_ROOT = path.join(__dirname, '..');
const OUT_PATH = path.join(REPO_ROOT, 'test', 'fixtures', 'codex-rollout-schema.json');
const TMP_PATH = path.join(REPO_ROOT, 'test', 'fixtures', '.codex-schema-staging.json');

/**
 * Probe whether the Codex CLI is installed and supports the
 * `app-server generate-internal-json-schema` subcommand. Returns true on
 * success, false otherwise. Errors are swallowed; the caller decides how
 * to fail.
 */
function probeCodexCli() {
  try {
    execSync('codex --version', { stdio: 'pipe', timeout: 5000 });
  } catch (_) {
    return { ok: false, reason: 'codex CLI is not installed (or not on PATH)' };
  }
  try {
    execSync('codex app-server generate-internal-json-schema --help', {
      stdio: 'pipe',
      timeout: 5000,
    });
  } catch (_) {
    return {
      ok: false,
      reason: 'codex CLI is installed but does not support `app-server generate-internal-json-schema`. ' +
        'This subcommand was added in Codex CLI >= 0.45 (PR openai/codex#14434). ' +
        'Upgrade with `npm i -g @openai/codex@latest`.',
    };
  }
  return { ok: true };
}

/**
 * Write a Codex-generated schema to OUT_PATH after light post-processing
 * (stable key order, trailing newline). Removes the staging file when done.
 */
function regenerate() {
  // Generate into a staging path so a partial failure does not corrupt OUT_PATH.
  execSync('codex app-server generate-internal-json-schema --out ' + JSON.stringify(TMP_PATH), {
    stdio: 'inherit',
    timeout: 30000,
  });

  let raw;
  try {
    raw = fs.readFileSync(TMP_PATH, 'utf-8');
  } catch (err) {
    throw new Error('Codex CLI succeeded but staging file is unreadable: ' + err.message);
  }

  let parsed;
  try {
    parsed = JSON.parse(raw);
  } catch (err) {
    throw new Error('Codex CLI emitted invalid JSON: ' + err.message);
  }

  // Preserve our hand-coded _meta block if it exists; tag the regen.
  let meta = {
    source: 'generated by `codex app-server generate-internal-json-schema`',
    generatedAt: new Date().toISOString(),
    phase: '17',
    plan: '17-01',
    requirementId: 'CDX-09',
  };
  try {
    const existing = JSON.parse(fs.readFileSync(OUT_PATH, 'utf-8'));
    if (existing && existing._meta && typeof existing._meta === 'object') {
      meta = Object.assign({}, existing._meta, meta);
    }
  } catch (_) {
    // No existing file or malformed; use defaults above.
  }

  parsed._meta = meta;

  // Stable serialization: pretty-print with 2-space indent, trailing newline.
  fs.writeFileSync(OUT_PATH, JSON.stringify(parsed, null, 2) + '\n', 'utf-8');

  // Clean up staging.
  try {
    fs.unlinkSync(TMP_PATH);
  } catch (_) { /* ignore */ }

  console.log('Wrote ' + OUT_PATH);
}

// ─── Main ──────────────────────────────────────────────────────────────────

const probe = probeCodexCli();
if (!probe.ok) {
  console.error('regen-codex-schema: ' + probe.reason);
  console.error('');
  console.error('The hand-coded fallback schema at ' + path.relative(REPO_ROOT, OUT_PATH));
  console.error('remains in place. Re-run this script on a machine with a current Codex CLI.');
  process.exit(1);
}

try {
  regenerate();
  process.exit(0);
} catch (err) {
  console.error('regen-codex-schema: regeneration failed: ' + err.message);
  process.exit(1);
}
